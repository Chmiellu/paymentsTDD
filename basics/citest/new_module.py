"""
Moduł zawiera funkcje do wykonywania podstawowych operacji matematycznych:
dodawania i odejmowania.
"""

def add(a: int, b: int) -> int:
    """
    Funkcja dodaje dwie liczby całkowite.

    Args:
        a (int): Pierwsza liczba.
        b (int): Druga liczba.

    Returns:
        int: Suma dwóch liczb.
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Funkcja odejmuje drugą liczbę od pierwszej.

    Args:
        a (int): Pierwsza liczba.
        b (int): Druga liczba.

    Returns:
        int: Różnica między pierwszą a drugą liczbą.
    """
    return a - b
